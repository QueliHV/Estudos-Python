#1.	Solicite ao usuário a idade com input() e diga se ele é:
#•	criança (< 12)
#•	adolescente (12–17)
#•	adulto (18+)

idade = int(input("Informe a sua idade:"))

if idade >= 18:
    print("Você é um adulto")
elif idade >= 12:
    print("Você é um adolescente")
else:
    print("Você é uma criança") 









